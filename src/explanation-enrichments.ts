export function withExplanationEnrichments(items: readonly Question[]): Question[] {
  return items.map((question) => {
    const enrichment = explanationEnrichments[question.id];
    const defaultEnrichment = defaultExplanationEnrichment(question);

    if (!enrichment && !defaultEnrichment) {
      return question;
    }

    return {
      ...question,
      misconception:
        question.misconception ?? enrichment?.misconception ?? defaultEnrichment?.misconception,
      flow: question.flow ?? enrichment?.flow ?? defaultEnrichment?.flow,
      story: question.story ?? enrichment?.story ?? defaultEnrichment?.story,
      nameOrigin: question.nameOrigin ?? enrichment?.nameOrigin ?? defaultEnrichment?.nameOrigin,
    };
  });
}
